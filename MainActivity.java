public class MainActivity {

    public double cmToMeter(double cm) {
        return cm / 100;
    }

    public double meterToCm(double meter) {
        return meter * 100;
    }

    public double gramToKg(double gram) {
        return gram / 1000;
    }

    public double kgToGram(double kg) {
        return kg * 1000;
    }
}
